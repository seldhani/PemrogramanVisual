    public void tampildata() {
 DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("NIM");
        tbl.addColumn("Nama");
        tbl.addColumn("Jurusan");
        tabHasil.setModel(tbl);

        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/pemvis", "root", "");
            Statement statement = conn.createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM mahasiswa");

            // Mengisi data ke tabel
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("nim"),
                    res.getString("nama"),
                    res.getString("jurusan")
                });
                tabHasil.setModel(tbl);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Terjadi kesalahan: " + e.getMessage());
        }
    }

