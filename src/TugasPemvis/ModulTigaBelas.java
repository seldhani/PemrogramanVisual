    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
     String nim = eNim.getText().trim();

    if (nim.isEmpty()) {
        JOptionPane.showMessageDialog(null, "NIM harus diisi untuk menghapus data");
        return;
    }

    String sql = "DELETE FROM mahasiswa WHERE nim = ?";

    try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/pemvis", "root", "");
         PreparedStatement pstmt = conn.prepareStatement(sql)) {

        pstmt.setString(1, nim);

        int rowsAffected = pstmt.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
            tampildata(); // Refresh tabel setelah data terhapus
            bersih(); // Bersihkan form
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
        }
    }   catch (java.sql.SQLException ex) {
            Logger.getLogger(ModulTigaBelas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_bHapusActionPerformed

